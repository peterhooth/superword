package org.apdplat.superword.system;

import org.apache.commons.lang.StringUtils;
import org.apdplat.word.recognition.RecognitionTool;

import javax.servlet.ServletException;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;
import java.io.IOException;

/**
 * word input autocomplete
 * Created by ysc on 12/11/15.
 */
public class InstantTipServlet extends HttpServlet {

    protected void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
        doGet(request, response);
    }

    protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
        response.setContentType("text/html");
        response.setCharacterEncoding("utf-8");
        String prefix = request.getParameter("prefix");
        if(StringUtils.isNotBlank(prefix)
                && RecognitionTool.isEnglish(prefix.replaceAll("\\s+", ""))){
            response.getWriter().write(InstantTip.getWordsByPrefix(prefix));
        }else {
            response.getWriter().write("");
        }
    }
}
